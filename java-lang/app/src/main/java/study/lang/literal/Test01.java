package study.lang.literal;

// 실습
// 리터럴을 사용하여 다음과 같이 출ㄹ겨되게 해라
// 출력
// 100
// 3.14
// 가
// 가나다
// true

// 기본 자료형(Primitive Data Type)의 값을 표현(Literal)하는 방법을 설명하라.

public class Test01 {

    public static void main(String[] args) {
        // 코드를 완성하라.
        System.out.println(100); // 정수 리터럴 100
        System.out.println(3.14); // 부동 소수점 리터럴 3.14
        System.out.println('가'); // 문자 '가'
        System.out.println("가나다"); // 문자열 "가나다"
        System.out.println(true); // 논리 true

    }

}
