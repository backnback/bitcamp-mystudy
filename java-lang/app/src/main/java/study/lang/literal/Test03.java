package study.lang.literal;

// 실습

// 부동소수점 3.14를 다음의 방식으로 표현하라.
// 3.14
// 31.4 x 10^-1
// 314 x 10^-2
// 0.314 x 10^1

public class Test03 {

    public static void main(String[] args) {
        // 코드를 완성하라.
        System.out.println(3.14); // 부동소수점
        System.out.println(31.4e-1); // 31.4 x 10^-1
        System.out.println(314e-2); // 314 x 10^-2
        System.out.println(0.314e1); // 0.314 x 10^1

    }

}
