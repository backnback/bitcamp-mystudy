package study.lang.literal;

// 실습

// 정수 리터럴을 다양한 지수법으로 표현해보라.
// 10진수 Literal 100을 지시에 따라 다양한 리터널로 표현하라.

public class Test02 {

    public static void main(String[] args) {
        // 코드를 완성하라.
        System.out.println(100); // 10진수
        System.out.println(0b01100100); // 2진수 (2진수 접두사 : 0b)
        System.out.println(0144); // 8진수 (8진수 접두사 : 0)
        System.out.println(0x64); // 16진수 (16진수 접두사 : 0x)

    }

}
